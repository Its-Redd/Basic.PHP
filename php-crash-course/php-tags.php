        <p>PHP code is written between the PHP tags. The default tags are:</p>
        <ul>
            <li><code>&lt;?php</code> - The opening tag</li>
            <li><code>?&gt;</code> - The closing tag</li>
        </ul>
        <p>Example:</p>
        <pre>
            <code>
                &lt;?php
                    echo "Hello, World!";
                ?&gt;
            </code>
